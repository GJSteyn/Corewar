#include "f_string.h"

int		f_toupper(int c)
{
	char chr;

	if (c == -1)
		return (-1);
	if (c >= 0 && c < 256)
	{
		chr = c;
		if (f_islower(chr))
			return (chr ^ 0x20);
	}
	return (c);
}
